import { formatGlossaryForPrompt } from "@/lib/rag/term-glossary";

export function getDipsOperatorContext(): string {
  const glossary = formatGlossaryForPrompt();

  return `## DIPS（ドローン情報基盤システム）補足知識
以下はマニュアル手順以外の、オペレーターが顧客説明に使える一般的なDIPS知識です。
回答の「DIPS補足」セクションで、マニュアル手順と明確に区別して活用してください。

### 制度・用語
- DIPS: 国土交通省が運用する無人航空機の登録・許可・通報等の情報基盤
- FPA（飛行許可・承認申請）: 条例・法規上の許可・承認が必要な飛行の申請
- FPR（飛行計画通報）: 許可・承認を受けた飛行の計画通報（飛行24時間前まで等の期限あり）
- 機体登録: 250g以上等の条件を満たす機体の登録（DIPS上の登録機能）
- 機体認証（UAC）: 個々の機体の安全性能を国が認める制度
- 型式認証: 無人航空機の型式（設計・性能）を国が認める制度
- 技能証明（LCA）: 操縦者の技能証明に関する手続
- eKYC: オンライン本人確認（マイナンバーカード等）

### オペレーター向け説明のポイント
- 申請・通報は DIPS 上で行い、受理後のステータスは DIPS 上で確認する
- 許可（FPA）と通報（FPR）は別手続。混同しないよう顧客に説明する
- 申請内容の不備がある場合は差戻し・修正依頼があり、期限に注意
- 代理人による手続は、依頼・解除の流れがマニュアルと異なる場合がある
- 最終的な法令適合・飛行可否の判断は国交省・地方整備局等の公式情報が正

### 回答時のルール（DIPS補足）
- マニュアル抜粋に書かれていない内容は「※DIPS補足」として記載する
- 推測や不確かな情報は書かない
- マニュアル手順と補足を混ぜず、セクションを分ける

${glossary}`;
}
